package com.dsp.student.model;

public class StuVO {

	private String stuNo;
	private String stuName;
	private String stuAge;
	private int stuGender;
	private String stuCollege;
	private String stuHometown;
	private String stuTel;
	public String getStuNo() {
		return stuNo;
	}
	public void setStuNo(String stuNo) {
		this.stuNo = stuNo;
	}
	
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	
	public String getStuAge() {
		return stuAge;
	}
	public void setStuAge(String stuAge) {
		this.stuAge = stuAge;
	}
	
	
	public int getStuGender() {
		return stuGender;
	}
	public void setStuGender(int stuGender) {
		this.stuGender = stuGender;
	}
	public String getStuCollege() {
		return stuCollege;
	}
	public void setStuCollege(String stuCollege) {
		this.stuCollege = stuCollege;
	}
	
	public String getStuHometown() {
		return stuHometown;
	}
	public void setStuHometown(String stuHometown) {
		this.stuHometown = stuHometown;
	}
	
	public String getStuTel() {
		return stuTel;
	}
	public void setStuTel(String stuTel) {
		this.stuTel = stuTel;
	}
	

}
